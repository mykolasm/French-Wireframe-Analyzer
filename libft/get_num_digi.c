#include "libft.h"

int		get_num_digi(long c)
{
	int size;

	size = 0;
	if (c < 0)
		size++;
	if (c == 0)
		return (1);
	while (c != 0)
	{
		c /= 10;
		size++;
	}
	return (size);
}
