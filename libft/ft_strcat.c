#include "libft.h"

char	*ft_strcat(char *dest, char *src)
{
	char *org_string;

	org_string = dest;
	while (*dest)
	{
		dest++;
	}
	while (*src)
	{
		*dest = *src;
		src++;
		dest++;
	}
	*dest = '\0';
	return (org_string);
}
